# % [markdown]
# ## Recoil ladder
#
# Each pulse's effective (Stark-corrected) detuning in recoil units. The
# alternating-beam pi pulses climb the momentum ladder one recoil at a time.

# %
pulses = [e for e in sequence if isinstance(e, seq.Pulse)]
eff_recoil = [
    (p.detuning_hz + p.probe_shift_coefficient * p.rabi_frequency**2)
    / sim.RECOIL_FREQUENCY_HZ
    for p in pulses
]
colours = ["tab:orange" if p.k == 1 else "tab:red" for p in pulses]

fig, ax = plt.subplots(figsize=(14, 5))
ax.bar(range(len(pulses)), eff_recoil, color=colours, width=0.9)
ax.axhline(0, color="0.3", lw=0.8)
ax.set_xlabel("Pulse index")
ax.set_ylabel("Effective detuning (recoil units)")
ax.set_title("RID 74397: shaped double-launch pulse ladder")
ax.legend(
    handles=[
        Patch(color="tab:orange", label="up (k=+1)"),
        Patch(color="tab:red", label="down (k=-1)"),
    ],
    frameon=False,
)

